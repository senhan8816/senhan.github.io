<blockquote>
<p>小程序开发对于前端开发工程师来说极其友好,因为小程序开发依然使用 CSS 和 JavaScript.但是也有一些区别.区别如下:</p>
</blockquote>
<ol>
<li>运行环境不同</li>
</ol>
<ul>
<li>小程序运行在微信环境(简单理解为微信)中</li>
<li>网页运行在浏览器中</li>
</ul>
<ol start="2">
<li>语言不同</li>
</ol>
<ul>
<li>小程序开发写界面使用 WXML(集合了基础组件和事件系统),网页开发使用 HTML(类似)</li>
<li>小程序开发写样式需要使用 WXSS,网页开发使用 CSS(但是 WXSS 和 CSS 高度相似)</li>
<li>小程序中虽然也使用 JavaScript,但是小程序不能操作 DOM 和 BOM(因为 DOM 和 BOM 是浏览器环境提供的 api). 微信环境为小程序提供<code>支付</code>,<code>获取用户信息</code>等微信特有的 api</li>
</ul>
<ol start="3">
<li>小程序开发需要在微信公众平台注册账号并使用微信开发工具进行开发</li>
</ol>
<h2 id="小程序与-app-开发的区别" tabindex="-1"><a class="header-anchor" href="#小程序与-app-开发的区别" aria-hidden="true">#</a> 小程序与 app 开发的区别</h2>
<ol>
<li>运行环境不同</li>
</ol>
<ul>
<li>小程序运行在微信环境(简单理解为微信)中</li>
<li>app 运行在 Android 或 iOS 系统环境中</li>
</ul>
<ol start="2">
<li>语言不同</li>
</ol>
<ul>
<li>小程序开发使用 JavaScript</li>
<li>Android 中开发使用 Java 或 Kotlin</li>
<li>iOS 中开发使用 Objective-C 或 Swift</li>
</ul>
<ol start="3">
<li>小程序体积小, app 体积较大</li>
</ol>
